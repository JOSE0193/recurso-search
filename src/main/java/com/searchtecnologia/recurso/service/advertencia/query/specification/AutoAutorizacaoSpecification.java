package com.searchtecnologia.recurso.service.advertencia.query.specification;

import com.searchtecnologia.recurso.model.advertencia.AutoAutorizacao;
import com.searchtecnologia.recurso.model.advertencia.AutoAutorizacao_;
import com.searchtecnologia.recurso.service.advertencia.query.criteria.AutoAutorizacaoCriteria;
import com.searchtecnologia.recurso.service.util.query.QueryService;
import org.springframework.data.jpa.domain.Specification;

import java.util.Objects;

public class AutoAutorizacaoSpecification {

    public static Specification<AutoAutorizacao> buildSpecification(AutoAutorizacaoCriteria criteria) {
        QueryService<AutoAutorizacao> queryService = new QueryService<>();
        Specification<AutoAutorizacao> specification = Specification.where(null);

        if (Objects.nonNull(criteria)) {
            // FILTRO NUMERO AUTO
            if (Objects.nonNull(criteria.getNumeroMulta())) {
                specification = specification.and(queryService.buildSpecification(criteria.getNumeroMulta(),
                        root -> root.get(AutoAutorizacao_.numeroMulta)));
            }
            // FILTRO NUMERO AUTO
            if (Objects.nonNull(criteria.getOrgaoAutuador())) {
                specification = specification.and(queryService.buildSpecification(criteria.getOrgaoAutuador(),
                        root -> root.get(AutoAutorizacao_.orgaoAutuador)));
            }
            // FILTRO NUMERO AUTO
            if (Objects.nonNull(criteria.getCodigoInfracao())) {
                specification = specification.and(queryService.buildSpecification(criteria.getCodigoInfracao(),
                        root -> root.get(AutoAutorizacao_.codigoInfracao)));
            }
        }
            return specification;
    }

}

































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































